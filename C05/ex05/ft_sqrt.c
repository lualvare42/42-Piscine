int	ft_sqrt(int nb)
{
	int	x;
	int	aux;

	aux = nb;
	x = 0;
	while (aux > 0)
	{
		aux = aux - (2 * x + 1);
		x++;
	}
	if (aux == 0)
		return (x);
	else
		return (0);
}
