int	ft_atoi(char *str)
{
	int	s;
	int	n;
	int	aux;

	s = 1;
	n = 0;
	aux = 0;
	while (str[n] && (str[n] == ' ' || (str[n] >= 9 && str[n] <= 13)))
		n++;
	while (str[n] && (str[n] == '-' || str[n] == '+'))
	{
		if (str[n] == '-')
			s *= -1;
		n++;
	}
	while (str[n] && (str[n] >= 48 && str[n] <= 57))
	{
		aux = (str[n] - 48) + (aux * 10);
		n++;
	}
	return (aux * s);
}
