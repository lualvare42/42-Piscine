int	ft_str_is_lowercase(char *str)
{
	int	n;
	int	a;

	n = 0;
	while (str[n] != '\0')
	{
		a = str[n];
		if (a >= 97 && a <= 122)
			n++;
		else
			return (0);
	}
	return (1);
}
